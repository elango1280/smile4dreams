//method to call the php file to get the gov services details from DB
function getGovServicesDetails(){

	var place = (document.oppform.optcity.options[document.oppform.optcity.selectedIndex].value);
	var services = document.getElementById("services").value;

	document.getElementById("state_error").style.display="none";
	document.getElementById("services_error").style.display="none";
		
	if ((place == null || place == ' ') && (services == null || services == '' || services == 'Services')){ 
  		document.getElementById("state_error").style.display="block";
  		document.getElementById("services_error").style.display="block";
		return;
  	} 
	if (place == null || place == ' '){ 
  		document.getElementById("state_error").style.display="block";
		return;
  	}
  	if (services == null || services == '' || services == 'Services'){ 
  		document.getElementById("services_error").style.display="block";
		return;
  	}
  	 	
	if (window.XMLHttpRequest){
		// code for IE7+, Firefox, Chrome, Opera, Safari
		xmlhttp=new XMLHttpRequest(); 
	}else{
		// code for IE6, IE5
		xmlhttp=new ActiveXObject("Microsoft.XMLHTTP");
  	}
  	
  	document.getElementById("resultset").innerHTML="<h2>Loading....</h2> <img src=http://smile4dreams.org/img/ajax-loader.gif>";

  	
	xmlhttp.onreadystatechange=function(){
		if (xmlhttp.readyState==4 && xmlhttp.status==200){
						
			if(xmlhttp.responseText == "")
				document.getElementById("resultset").innerHTML="<img align='middle' src='/img/unknownorg.jpg' title='Invalid'><br><br><h2>No Result found!!</h2>Please check the services details entered.";
			else		
				document.getElementById("resultset").innerHTML=xmlhttp.responseText;
    		}
  	}
	xmlhttp.open("GET","getservices.php?place="+place+"&services="+services,true); 
	xmlhttp.send();
}

</script>

